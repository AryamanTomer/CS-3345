/*  Binary Tree:

A binary tree is a hierarchical data 
structure in which each node has at most
two children, referred to as the left child
and the right child.

*/
class TreeNode {
    int data;
    TreeNode left;
    TreeNode right;

    public TreeNode(int data) {
        this.data = data;
        this.left = null;
        this.right = null;
    }
}